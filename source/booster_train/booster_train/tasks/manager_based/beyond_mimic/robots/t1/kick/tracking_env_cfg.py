ACTOR_OBS_HISTORY_LENGTH = 10


class PolicyObsHistoryTerm(ManagerTermBase):
    """History-stacked policy observation matching deploy-side frame stacking."""

    def __init__(self, cfg: ObsTerm, env):
        super().__init__(cfg, env)
        self.history_length = ACTOR_OBS_HISTORY_LENGTH
        self.obs_history = None

    def reset(self, env_ids: torch.Tensor | None = None):
        if self.obs_history is None:
            return
        if env_ids is None:
            env_ids = slice(None)
        self.obs_history[env_ids] = 0.0

    @staticmethod
    def _add_uniform_noise(obs: torch.Tensor, low: float, high: float) -> torch.Tensor:
        return obs + torch.empty_like(obs).uniform_(low, high)

    def __call__(self, env, command_name: str) -> torch.Tensor:
        command = mdp.generated_commands(env, command_name=command_name)
        motion_anchor_ori_b = self._add_uniform_noise(mdp.motion_anchor_ori_b(env, command_name=command_name), -0.05, 0.05)
        base_ang_vel = self._add_uniform_noise(mdp.base_ang_vel(env), -0.2, 0.2)
        joint_pos = self._add_uniform_noise(mdp.joint_pos_rel(env), -0.01, 0.01)
        joint_vel = self._add_uniform_noise(mdp.joint_vel_rel(env), -0.5, 0.5)
        actions = mdp.last_action(env)

        single_obs = torch.cat(
            (
                command,
                motion_anchor_ori_b,
                base_ang_vel,
                joint_pos,
                joint_vel,
                actions,
            ),
            dim=-1,
        ).clamp_(-100.0, 100.0)

        if self.obs_history is None or self.obs_history.shape[2] != single_obs.shape[1]:
            self.obs_history = torch.zeros(
                env.num_envs,
                self.history_length,
                single_obs.shape[1],
                dtype=single_obs.dtype,
                device=single_obs.device,
            )

        self.obs_history = self.obs_history.roll(shifts=-1, dims=1)
        self.obs_history[:, -1] = single_obs
        return self.obs_history.reshape(env.num_envs, -1)


